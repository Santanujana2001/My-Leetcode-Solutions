class Solution
{
    public:
    Node* reverseBetween(Node* head, int m, int n)
    {
         Node* ans = new Node(0);
       ans->next=head;
       Node* prev = ans;
       int k=m;
       while(k-1){
           prev=prev->next;
           k--;
       }
           Node* cur = prev->next;
           Node* temp = cur->next;
           for(int i=1;i<=n-m;i++){
             cur->next=temp->next;
             temp->next= prev->next;
             prev->next=temp;
             temp= cur->next;
           }
       return ans->next;
    }
};

